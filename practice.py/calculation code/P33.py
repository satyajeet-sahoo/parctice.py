#WAPP to swap two Numbers By using arithmetic operators multiplication and division operator
P=int(input("Please enter value for P: "))  
Q=int(input("Please enter value for Q: "))  
print("The Value of P before swapping: ",P)  
print("The Value of Q before swapping: ",Q)
#By using multiplication and division operator
P=P*Q    
Q=P/Q   
P=P/Q  
print("The Value of P after swapping: ",P)  
print("The Value of Q after swapping: ",Q)  